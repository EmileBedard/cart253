"use strict";

/**
 * creates the canvas for the blank page simulation
 */
function setup() {
    // happens once at the beginning
    // creates the standard resolution canvas
    createCanvas(640, 480,);
}
/**
 * draws the scary blank page
 */
function draw() {
    // happens every frame! ;)
    // created the yellow desk
    background(255, 255, 120);
    // the blank piece of paper
    rect(180, 120, 300, 300,)

}